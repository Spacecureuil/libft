#include "libft.h"

char	*ft_itoa(int n)
{
	int		val;
	int		i;
	char	buff[12];

	i = 11;
	buff[i--] = 0;
	if (!n)
		buff[i--] = '0';
	val = n;
	if (n < 0)
		n = -n;
	if (n < 0)
		return (ft_strdup("-2147483648"));
	while (n)
	{
		buff[i--] = n % 10 + '0';
		n /= 10;
	}
	if (val < 0)
		buff[i--] = '-';
	return (ft_strdup(buff + i + 1));
}
